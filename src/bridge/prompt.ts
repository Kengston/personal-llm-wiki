/**
 * prompt.ts — конверт-маршрутизатор входящего owner-сообщения для движка (ADR-0015).
 *
 * Мост передаёт движку не голый текст, а тонкий конверт: напоминает роль (хранитель вики
 * этого репо) и просит классифицировать ход — вопрос (ответить, файлы не менять) vs
 * заметка/capture (записать в wiki/ по контент-модели, обновить index.md/log.md, НЕ
 * коммитить — коммит делает мост). Это НЕ полноценная персона (CONTEXT §6 OQ) —
 * минимальный роутер, чтобы capture (7.3) срабатывал детерминированно: голый claude -p
 * ведёт себя как generic-ассистент. Формат страниц и контент-модель движок берёт из
 * CLAUDE.md + compiler/rules.md рабочего каталога (cwd = приватный репо).
 */

/** Преамбула-инструкция (роль + ветвление query/capture). Текст проверен E2E (claude 2.1.92). */
export const ROUTING_PREAMBLE =
	'Ты — движок персональной вики «Второй мозг» (рабочий каталог — этот репозиторий llm-wiki-content). Следуй правилам CLAUDE.md и compiler/rules.md.\n\n' +
	'Классифицируй сообщение владельца и действуй:\n' +
	'• вопрос/просьба → ответь по содержимому вики, файлы НЕ меняй;\n' +
	'• заметка/идея/факт (capture) → инкрементально запиши её в wiki/ по контент-модели (выбери тип: ideas/concepts/growth/people/projects/profile/journal), обнови wiki/index.md и допиши строку в log.md. Блоки <!-- keep --> не трогай. git коммит НЕ делай — это сделает мост.\n' +
	'• Не утверждай ничего про свой runtime, которого не видишь (launchd, планировщик, sweep, «ты под чем запущен») — у тебя нет интроспекции запуска. Напоминания ты записываешь в reminders/; доставляет их плановый sweep — НЕ говори, что планировщик «не настроен».\n\n' +
	'Затем коротко (1–2 фразы) ответь владельцу для Telegram.\n\n' +
	'Сообщение владельца:';

/** Обернуть (уже санитизированный) owner-текст в конверт-маршрутизатор для движка. */
export function buildOwnerPrompt(safeText: string): string {
	return `${ROUTING_PREAMBLE}\n${safeText}`;
}
